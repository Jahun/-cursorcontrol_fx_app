public class Coordinates {

    private double mouseX;
    private double mouseY;

    public Coordinates(double mouseX, double mouseY) {
        this.mouseX = mouseX;
        this.mouseY = mouseY;
    }


    public double getMouseY() {
        return mouseY;
    }

    public double getMouseX() {
        return mouseX;
    }

}
